import turtle

t = turtle.Turtle()

t.forward(100)      # Move 100 pixels forward
t.left(90)          # Turn 90 degrees to the left
t.forward(100)      # Move 100 pixels forward
t.left(90)          # Turn 90 degrees to the left
t.forward(100)      # Move 100 pixels forward
t.left(90)          # Turn 90 degrees to the left
t.forward(100)      # Move 100 pixels forward
t.left(90)          # Go back to the start